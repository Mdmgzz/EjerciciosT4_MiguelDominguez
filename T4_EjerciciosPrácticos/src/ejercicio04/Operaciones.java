package ejercicio04;

public class Operaciones {
	int num1;
	int num2;
	
	public int suma () {
		int resultado;
		
		resultado=num1+num2;
		return resultado;
	}
	
	public int resta () {
		int resultado;
		
		resultado=num1-num2;
		return resultado;
	}
	public int multiplicacion () {
		int resultado;
		
		resultado=num1*num2;
		return resultado;
	}
	public int division () {
		int resultado;
		
		resultado=num1/num2;
		return resultado;
	}
}

